
namespace HappyDummyWFA
{
    class MovimentKeyboard
    {

        public static string movimentRight()
        {
            return "{+}{D}";
        }

        public static string movimentLeft()
        {
            return "{+}{A}";
        }

        public static string movimentNorth()
        {
            return "{+}{W}";
        }

        public static string movimentSouth()
        {
            return "{+}{S}";
        }

    }
}
